def to_requests_settings(settings):
    ret = {}
    args = ["timeout", "verify", "proxies", "headers", "cookies"]
    for i in args:
        ret[i] = settings[i]

    return ret


class Session(requests.Session):
    def __init__(self, **kwargs):
        super().__init__()

        self.lock = Lock()

        self.get_renew(False, kwargs["user-agent"] is None)

    @staticmethod
    def new_useragent():
        r = random.randint(0, 9)
        if r == 0:
            agents = useragents["opera"]
        elif r >= 1 and r <= 3:
            agents = useragents["mozilla"]
        else:
            agents = useragents["google"]

        return agents[random.randint(0, len(agents) - 1)]

    def get_renew(self, clear=True, user_agent=True):
        if clear:
            self.close()

        if user_agent:
            self.headers.update({"User-Agent": self.new_useragent()})

    def get_req_try(self, url, settings, retry=False):
        if not retry:
            if settings["wait"] != 0:
                time.sleep(settings["wait"])
            if settings["wait_random"] != 0:
                time.sleep(random.randint(0, settings["wait_random"] + 1) / 1000)

        if settings["logger"]:
            if isinstance(settings["logger"], list):
                settings["logger"].append(url)
            else:
                print(url, file=settings["logger"])

        return self.get(url, **to_requests_settings(settings))

    def get_req(self, url, settings, state):
        with self.lock:
            if url in state["visited"]:
                raise AlreadyVisitedError(url)

            state["visited"].add(url)

        tries = settings["retries"]
        retry_wait = settings["retry_wait"]

        instant_end_code = [400, 401, 402, 403, 404, 410, 412, 414, 421, 505]

        i = 0
        while True:
            try:
                resp = self.get_req_try(url, settings, i != 0)
            except (
                requests.ConnectTimeout,
                requests.ConnectionError,
                requests.ReadTimeout,
                requests.exceptions.ChunkedEncodingError,
                RequestError,
            ):
                resp = None

            if resp is None or not (
                resp.status_code >= 200 and resp.status_code <= 299
            ):
                if resp is not None and resp.status_code in instant_end_code:
                    raise RequestError(
                        "failed completely {} {}".format(resp.status_code, url)
                    )
                if i >= tries:
                    raise RequestError(
                        "failed {} {}".format(
                            "connection" if resp is None else resp.status_code, url
                        )
                    )
                i += 1
                if retry_wait != 0:
                    time.sleep(retry_wait)
            else:
                return resp

    def get_html(self, url, settings, state, trim=False, return_cookies=False):
        resp = self.get_req(url, settings, state)

        r = resp.text
        if trim:
            r = r.translate(str.maketrans("", "", "\t\n\r\a"))

        rq = reliq(r)

        if return_cookies:
            return [rq, resp.cookies.get_dict()]
        return rq

    def get_json(self, url, settings, state):
        resp = self.get_req(url, settings, state)
        return resp.json()
